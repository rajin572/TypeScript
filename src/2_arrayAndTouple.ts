/* 
---------------------------------Array And Touple----------------------------


Tuple: TypeScript tuples are like arrays with a fixed number of elements. They provide us with a fixed size container that can store values of multiple types, where the order and structure are very important. This data type is best used when we know exactly how many types we want to allow in an array. 

_____________________________________________________________________________________________________________
*/

//____________________Array___________________

let names: string[] = ["dada", "nana"];
let roll: number[] = [10, 20];
let checking: boolean[] = [true, false];

//___________________Touple___________________
let data: [number, string] = [1, "sampoo"];
let data2: [number, string, boolean] = [10, "Hero", false];
